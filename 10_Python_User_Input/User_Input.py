
# What is User Input in Python?

# User input means getting input from the user, in the form of text, numbers, or other data types.

# Python 3 allows us to get user input.

# The input() function allows us to get input from the user.

# The input() function reads a line from input, converts the line into a string, and returns the string.

# Syntax

# input(prompt)

# Parameter Values

# Parameter	Description

# prompt	A String, representing a default message before the input.

# Example

# Get input from the user:

x = input("Enter your name: ")

print("Hello, " + x)


